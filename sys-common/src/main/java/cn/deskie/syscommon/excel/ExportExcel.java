    public static void createPlanExcel(String filename, List list, Map<String, String> title) {
        String[] headers = {"姓名","电话","地址","错误原因"};
        // 创建Excel的工作书册 Workbook,对应到一个excel文档
        HSSFWorkbook wb = new HSSFWorkbook();
        // 创建Excel的工作sheet,对应到一个excel文档的tab
        HSSFSheet sheet = wb.createSheet("sheet0");
        // 创建Excel的sheet的第一行
        HSSFRow rowtitle = sheet.createRow(0);

        rowtitle.setHeightInPoints(40);

        //设置列宽
        //37.5差不多相当于一个像素

        for (int i = 0; i < 2; i++) {
            sheet.setColumnWidth(i, (int) (37.5 * 80));
        }
        for (int i = 2; i < list.size(); i++) {
            sheet.setColumnWidth(i, (int) (37.5 * 150));
        }



        //写入表头
        int k = 0;
        for (String key : title.keySet()) {
            HSSFCell celltitle = rowtitle.createCell(k);
            celltitle.setCellValue(title.get(key));
            k++;
        }


        //写入列表内容
        int size = list.size();

        // 设置单元格内容格式
        HSSFCellStyle datestyle = wb.createCellStyle();

        datestyle.setAlignment(HSSFCellStyle.ALIGN_CENTER);//水平居中
        datestyle.setVerticalAlignment(HSSFCellStyle.VERTICAL_CENTER);//垂直居中
        datestyle.setBorderBottom(HSSFCellStyle.BORDER_THIN); //下边框
        datestyle.setBorderLeft(HSSFCellStyle.BORDER_THIN);//左边框
        datestyle.setBorderTop(HSSFCellStyle.BORDER_THIN);//上边框
        datestyle.setBorderRight(HSSFCellStyle.BORDER_THIN);//右边框
        datestyle.setWrapText(true);//设置自动换行
        for (int i = 0; i < size; i++) {
            Map bot = (Map) list.get(i);
            HSSFRow row = sheet.createRow(i + 1);
            row.setHeightInPoints(60);
            //列
            int j = 0;
            for (String key : title.keySet()) {
                HSSFCell cell = row.createCell(j);
                cell.setCellStyle(datestyle);
                if (!bot.get(key).equals("") && bot.get(key) != null) {
                    cell.setCellValue(bot.get(key).toString());
                }
                j++;
            }
        }
        //生成文件
        File excel = new File(filename);
        FileOutputStream os = null;
        try {
            os = new FileOutputStream(excel);
            wb.write(os);
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        } finally {
            try {
                if (os != null) {
                    os.close();
                }
            } catch (IOException e) {
                e.printStackTrace();
            }
        }

    }
    public static void downExcel(String filename, HttpServletRequest request, HttpServletResponse response) {
        File tempFile;

        tempFile = new File(filename);

        if (!tempFile.exists()) {
            try {
                throw new Exception("excel file not found!");
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        // 创建一个缓冲输入流对象
        BufferedInputStream br;
        try {
            br = new BufferedInputStream(new FileInputStream(tempFile));
            byte[] buf = new byte[1024];
            int len = 0;

            response.reset(); // 非常重要
            response.setContentType("application/x-msdownload");
            response.setCharacterEncoding("utf-8");

            String tempName = tempFile.getName();
            if (request.getHeader("User-Agent").toLowerCase().indexOf("firefox") > 0) {//ff
                tempName = new String(tempName.getBytes("UTF-8"), "ISO8859-1");
            } else if (request.getHeader("User-Agent").toUpperCase().indexOf("MSIE") > 0) {//ie
                tempName = URLEncoder.encode(tempName, "UTF-8");
            } else if (request.getHeader("User-Agent").toLowerCase().indexOf("chrome") > 0) {//chrome
                tempName = new String(tempName.getBytes("UTF-8"), "ISO8859-1");
            } else {
                tempName = new String(tempName.getBytes("UTF-8"), "ISO8859-1");
            }
            response.setHeader("Content-Disposition", "attachment; filename=" + tempName);

            // 创建输出流对象
            OutputStream outStream = response.getOutputStream();

            // 开始输出
            while ((len = br.read(buf)) > 0) {
                outStream.write(buf, 0, len);
            }

            // 关闭流对象
            br.close();
            outStream.close();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        } catch (IOException e) {
            e.printStackTrace();
        }
        // 最后删除临时文件
        if (tempFile.exists()) {
            tempFile.delete();
        }
    }


